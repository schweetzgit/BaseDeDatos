    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        String isbn = txt_isbn.getText();
        String nombre = txt_nombre.getText();
        int pag = Integer.parseInt(txt_pag.getText());
        String editorial = txt_editorial.getText();
        int edicion = Integer.parseInt(txt_edicion.getText());
        String idioma = txt_idioma.getText();
        String enc = (String) box_enc.getSelectedItem();
        
        String libro_isbn = txt_isbn.getText();
        String seccion = (String) box_sec.getSelectedItem();
        String mov = "Registro";
        String fecha = fecha();
        int usuario = Integer.parseInt(txt_usuario.getText());
        
        try{
            Connection conn = getConnection();
            if (0 == verificar()){
                limpiar();
            }else{
                PreparedStatement ps = conn.prepareStatement("INSERT INTO libro (ISBN, nombre, paginas, editorial, edicion, idioma, encuadernacion) VALUES (?,?,?,?,?,?,?)");
                PreparedStatement ps2 = conn.prepareStatement("INSERT INTO registro (seccion, usuario_emp, libro_ISBN, movimiento, fecha) VALUES (?,?,?,?,?)");

                ps.setString(1, isbn);
                ps.setString(2, nombre);
                ps.setInt(3, pag);
                ps.setString(4, editorial);
                ps.setInt(5, edicion);
                ps.setString(6, idioma);
                ps.setString(7, enc);
                ps.executeUpdate();
                
                ps2.setString(1, seccion);
                ps2.setInt(2, usuario);
                ps2.setString(3, libro_isbn);
                ps2.setString(4, mov);
                ps2.setString(5, fecha);
                ps2.executeUpdate();

                JOptionPane.showMessageDialog(null,"Registro guardado");
                limpiar();
                cargarTabla();
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

