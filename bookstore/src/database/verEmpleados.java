    private void tbl_empleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_empleadosMouseClicked
        try{
            int fila = tbl_empleados.getSelectedRow();
            String isbn = tbl_empleados.getValueAt(fila, 0).toString();
            PreparedStatement ps;
            ResultSet rs;

            Connection conn = getConnection();
            ps = conn.prepareStatement("SELECT contrasena, nombre, apellidos, rfc, telefono, direccion, email FROM empleado WHERE usuario=?");
            ps.setString(1, isbn);
            rs = ps.executeQuery();

            while(rs.next()){
                txt_usuario.setText(String.valueOf(isbn));
                txt_pass.setText(rs.getString("contrasena"));
                txt_nombre.setText(rs.getString("nombre"));
                txt_apellidos.setText(rs.getString("apellidos"));
                txt_rfc.setText(rs.getString("rfc"));
                txt_telefono.setText(rs.getString("telefono"));
                txt_dir.setText(rs.getString("direccion"));
                txt_email.setText(rs.getString("email"));  
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_tbl_empleadosMouseClicked
