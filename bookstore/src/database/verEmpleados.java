    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        String usuario = txt_usuario.getText();
        String pass = txt_pass.getText();
        String nombre = txt_nombre.getText();
        String apellidos = txt_apellidos.getText();
        String rfc = txt_rfc.getText();
        String telefono = txt_telefono.getText();
        String dir = txt_dir.getText();
        String email = txt_email.getText();

        try{
            Connection con = getConnection();
            PreparedStatement ps = con.prepareStatement("UPDATE empleado SET contrasena=?, nombre=?, apellidos=?, rfc=?, telefono=?, direccion=?, email=? WHERE usuario=?");
            ps.setString(1, pass);
            ps.setString(2, nombre);
            ps.setString(3, apellidos);
            ps.setString(4, rfc);
            ps.setString(5, telefono);
            ps.setString(6, dir);
            ps.setString(7, email);
            ps.setString(8, usuario);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Registro actualizado");
            limpiar();
            cargarTabla();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e.toString());}
    }//GEN-LAST:event_btn_modificarActionPerformed

